package class2;

public class E9ArithmeticOperators {
    public static void main(String[] args) {

        double num1=5;
        double num2=7;
        double addResults=num1+num2;
        System.out.println("The addition of two numbers "+num1+" and "+num2+ " is equal to "+addResults);

        double subtractResults=num2-num1;
        System.out.println("The subtract of two numbers "+num1+" and "+num2+" is equal to "+subtractResults);

        double divisionResults=num1/num2;
        System.out.println("The division of two numbers "+num1+" and "+num2+" is equal to "+divisionResults);

        double multiplicationResults=num1*num2;
        System.out.println("The multipliacation of two numbers "+num1+" and "+num2+" is equal to "+multiplicationResults);






    }
}
