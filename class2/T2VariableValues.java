package class2;

public class T2VariableValues {
    public static void main(String[] args) {

        byte age=78;
        age=80;
        System.out.println(age);
        short price=1000;
        price=1100;
        System.out.println(price);
        int salary=1300000;
        salary=14000000;
        System.out.println(salary);
        long distance=123234456666L;
        distance=127789000444l;
        System.out.println(distance);
        float number1=12451.111F;
        number1=128990.456F;
        System.out.println(number1);
        double num2=1325.789;
        num2=12.34;
        System.out.println(num2);
        char symbol='%';
        symbol='#';
        System.out.println(symbol);
        boolean isRich=false;
        isRich=true;
        System.out.println(isRich);

    }
}
